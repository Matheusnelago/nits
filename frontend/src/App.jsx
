
import { BrowserRouter, Routes, Route, Navigate } from 'react-router-dom'
import { AuthProvider } from './context/useAuth'
import LoginPage from './components/Auth/LoginPage'
import ProtectedRoute from './components/Auth/ProtectedRoute'
import AdminDashboard from './components/Dashboard/AdminDashboard'
import SettingsPage from './components/SettingsPage/SettingsPage'
import TrafficManagement from './components/TrafficManagement/TrafficManagement'
import OfficerManagement from './components/OfficerManagement/OfficerManagement'
import HelpCenter from './components/HelpCenter/HelpCenter'
import Layout from './components/Layout/Layout'
import { useState } from 'react'

function AppContent() {
  const [currentPage, setCurrentPage] = useState('dashboard')

  const renderPage = () => {
    switch(currentPage) {
      case 'dashboard':
        return <AdminDashboard />
      case 'settings':
        return <SettingsPage />
      case 'tickets_management':
        return <TrafficManagement />
      case 'Officer_management':
        return <OfficerManagement />
      case 'help_center':
        return <HelpCenter />
      default:
        return <AdminDashboard />
    }
  }

  return (
    <Layout>
      {renderPage()}
    </Layout>
  )
}

function App() {
  return (
    <BrowserRouter>
      <AuthProvider>
        <Routes>
          <Route path="/login" element={<LoginPage />} />
          <Route
            path="/*"
            element={
              <ProtectedRoute>
                <AppContent />
              </ProtectedRoute>
            }
          />
        </Routes>
      </AuthProvider>
    </BrowserRouter>
  )
}

export default App

